def get_edge_segments(pos, faces):
    # Extract the triangles and create segments for the three edges of each face
    return pos[faces[:, [0, 1, 1, 2, 2, 0]]].reshape(-1, 2, 3)

def update(frame):
    global positions, velocities
    velocities, positions = step(
        velocities,
        positions,
        neighbours,
        cotangent_weights,
        dt=dt,
        g=g,
        friction_coeff=friction_coeff,
        mu=mu,
        nabla=nabla,
    )
    # Update point positions
    sc._offsets3d = (positions[:, 0], positions[:, 1], positions[:, 2])

    # Update line segments for the edges
    segments = get_edge_segments(positions, faces)
    lines.set_segments(segments)

    return sc, lines

